# Complex Boolean Expressions
# If statements sometimes use more complicated boolean expressions for their conditions.
# They may contain multiple comparisons operators, logical operators, and even calculations. Examples:

# if 18.5 <= weight / height**2 < 25:
#     print("BMI is considered 'normal'")
#
# if is_raining and is_sunny:
#     print("Is there a rainbow?")

# if (not unsubscribed) and (location == "USA" or location == "CAN"):
#     print("send email")

# For really complicated conditions you might need to combine some ands, ors and nots together.
# Use parentheses if you need to make the combinations clear.

# However simple or complex, the condition in an if statement must be a boolean expression that
# evaluates to either True or False and it is this value that decides
# whether the indented block in an if statement executes or not.
